#!/usr/bin/env node

/**
 * Generate firebase-config.js from environment variables
 * This is used during the build process for GitHub Pages deployment
 * 
 * Required environment variables:
 * - FIREBASE_API_KEY
 * - FIREBASE_AUTH_DOMAIN
 * - FIREBASE_DATABASE_URL
 * - FIREBASE_PROJECT_ID
 * - FIREBASE_STORAGE_BUCKET
 * - FIREBASE_MESSAGING_SENDER_ID
 * - FIREBASE_APP_ID
 */

const fs = require('fs');
const path = require('path');

// Check if all required environment variables are set
const requiredVars = [
    'FIREBASE_API_KEY',
    'FIREBASE_AUTH_DOMAIN',
    'FIREBASE_DATABASE_URL',
    'FIREBASE_PROJECT_ID',
    'FIREBASE_STORAGE_BUCKET',
    'FIREBASE_MESSAGING_SENDER_ID',
    'FIREBASE_APP_ID'
];

const missingVars = requiredVars.filter(varName => !process.env[varName]);

if (missingVars.length > 0) {
    console.error('❌ Error: Missing required environment variables:');
    missingVars.forEach(varName => console.error(`   - ${varName}`));
    console.error('\nFor local development, copy client/firebase-config.js.example to client/firebase-config.js');
    console.error('For GitHub Pages deployment, set these as GitHub Secrets');
    process.exit(1);
}

// Generate the firebase-config.js content
const configContent = `// Firebase Configuration
// This file is auto-generated during the build process
// DO NOT edit this file manually - it will be overwritten

const firebaseConfig = {
    apiKey: "${process.env.FIREBASE_API_KEY}",
    authDomain: "${process.env.FIREBASE_AUTH_DOMAIN}",
    databaseURL: "${process.env.FIREBASE_DATABASE_URL}",
    projectId: "${process.env.FIREBASE_PROJECT_ID}",
    storageBucket: "${process.env.FIREBASE_STORAGE_BUCKET}",
    messagingSenderId: "${process.env.FIREBASE_MESSAGING_SENDER_ID}",
    appId: "${process.env.FIREBASE_APP_ID}"
};

// Export the configuration
export { firebaseConfig };
`;

// Determine output path (public/firebase-config.js for deployment build)
const outputPath = path.join(__dirname, '..', 'public', 'firebase-config.js');

// Ensure public directory exists
const publicDir = path.join(__dirname, '..', 'public');
if (!fs.existsSync(publicDir)) {
    fs.mkdirSync(publicDir, { recursive: true });
}

// Write the file
fs.writeFileSync(outputPath, configContent, 'utf8');

console.log('✅ Firebase configuration generated successfully at:', outputPath);
console.log('   Using project:', process.env.FIREBASE_PROJECT_ID);
